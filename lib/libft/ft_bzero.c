#include "libft.h"
#include <stdio.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*str;
	size_t			i;

	str = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		str[i] = 0;
		i++;
	}
}

int	main(void)
{
	char	str[10] = "abcdefghi";
	ft_bzero(str, 6);
	printf("%s", str);
	return (0);
}
